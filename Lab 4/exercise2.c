uint32_t adc_measure()
{
    LED_PORT &= ~_BV(LED);
    ADCSRA |= _BV(ADSC); // wykonaj konwersję
    while (!(ADCSRA & _BV(ADIF)))
        ;
    ADCSRA |= _BV(ADIF); // wyczyść bit ADIF (pisząc 1!)
    uint32_t v = ADC;    // weź zmierzoną wartość (0..1023)

    return v;
}

// Nawet z latarką przyłożoną ma >200, więc trochę przesunięte
uint32_t translateRate(uint32_t v)
{
    v = (v * v) / NUM_5V;
    uint16_t val = pgm_read_word(&gamma_table[v]);
    // Inverted
    return (val + val / 3 < 1024) ? ((NUM_5V - (uint32_t)val - (uint32_t)val / 3)) : (0);
}

void ledCycle(uint32_t v)
{
    for (uint32_t i = 0; i < NUM_CYCLES; ++i)
    {
        if (v > 0)
        {
            LED_PORT |= _BV(LED);
            delay_us_timer(BASE_DELAY * v / NUM_5V);
        }
        if (v < NUM_5V)
        {
            LED_PORT &= ~_BV(LED);
            delay_us_timer(BASE_DELAY * (NUM_5V - v) / NUM_5V);
        }
    }
}

int main()
{
    LED_DDR |= _BV(LED);
    // zainicjalizuj ADC
    ctc_init();
    adc_init();

    while (1)
    {
        uint32_t v = translateRate(adc_measure());
        ledCycle(v);
    }
}